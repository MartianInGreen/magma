<script>
export default {
    data() {
        return {
            themeSwitcher: null,
        }
    },
    methods: {
        setThemeLight() {
            document.cookie = 'theme=light; path=/;';
            console.log('Set theme to light');
            document.documentElement.setAttribute('data-theme', 'light');
        },
        setThemeDark() {
            document.cookie = 'theme=dark; path=/;';
            console.log('Set theme to dark');
            document.documentElement.setAttribute('data-theme', 'dark');
        },
        getCookie(name) {
            var nameEQ = name + "=";
            var ca = document.cookie.split(';');
            for(var i=0;i < ca.length;i++) {
                var c = ca[i];
                while (c.charAt(0)==' ') c = c.substring(1,c.length);
                if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length,c.length);
            }
            return null;
        },
    },
    mounted() {
        this.themeSwitcher = document.getElementById('themeSwitcher');

        var theme = this.getCookie('theme');
        if (theme) {
            if (theme === 'dark') {
                this.themeSwitcher.checked = true;
                this.setThemeDark();
            } else {
                this.themeSwitcher.checked = false;
                this.setThemeLight();
            }
        }

        this.themeSwitcher.addEventListener('change', () => {
            if (this.themeSwitcher.checked) {
                this.setThemeDark();
            } else {
                this.setThemeLight();
            }
        });
    }
}
</script>